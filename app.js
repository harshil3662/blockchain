window.addEventListener('load', async () => {
    // Connect to Metamask
    if (typeof window.ethereum !== 'undefined') {
        window.web3 = new Web3(window.ethereum);
        await window.ethereum.enable();
    }

    // Display the wallet address
    const accounts = await web3.eth.getAccounts();
    const walletAddress = document.getElementById('walletAddress');
    walletAddress.innerText = `Wallet Address: ${accounts[0]}`;

    // Display NFTs owned by the user
    const nfts = await getOwnedNFTs(accounts[0]);
    displayNFTs(nfts);
});

async function getOwnedNFTs(ownerAddress) {
    const contract = new web3.eth.Contract(contractABI, contractAddress);
    const balance = await contract.methods.balanceOf(ownerAddress).call();
    const nfts = [];

    for (let i = 0; i < balance; i++) {
        const tokenId = await contract.methods.tokenOfOwnerByIndex(ownerAddress, i).call();
        const tokenURI = await contract.methods.tokenURI(tokenId).call();
        nfts.push({ tokenId, tokenURI });
    }

    return nfts;
}

function displayNFTs(nfts) {
    const nftsContainer = document.getElementById('nfts');
    nftsContainer.innerHTML = '';

    for (const nft of nfts) {
        const nftElement = document.createElement('div');
        nftElement.innerHTML = `<div>
            <img src="${nft.tokenURI}" width="100" height="100">
            <p>Token ID: ${nft.tokenId}</p>
        </div>`;
        nftsContainer.appendChild(nftElement);
    }
}

async function mintNFT() {
    const ipfsUrl = document.getElementById('ipfsUrl').value;
    const contract = new web3.eth.Contract(contractABI, contractAddress);
    const accounts = await web3.eth.getAccounts();

    // Mint a new NFT
    contract.methods.mintNFT(accounts[0], ipfsUrl).send({ from: accounts[0] })
        .on('transactionHash', (hash) => {
            console.log(`Transaction Hash: ${hash}`);
            alert('NFT Minted Successfully!');
        })
        .on('error', (error) => {
            console.error(error);
            alert('Error Minting NFT');
        });
}
